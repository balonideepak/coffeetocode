package ds.linkedlist.doublylinklist;

public class Link implements Cloneable{
	public int data;
	public Link next;
	public Link previous;

	public Link(int data) {
		this.data = data;
	}
	
	@Override
	protected Object clone()  {
		try {
			return super.clone();
		} catch (CloneNotSupportedException e) {
			e.printStackTrace();
			return null;
		}
	}
}
